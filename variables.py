# This is a comment. Comments are for humans, not Python.

# Text (string)
name = "Ivan"
print("My name is:", name)

# Number (integer)
age = 25
print("My age is:", age)

# Decimal number (float)
height = 1.75
print("My height in meters is:", height)

# True/False (boolean)
is_student = True
print("Am I a student?", is_student)

# Combine variables
print(name, "is", age, "years old and", height, "meters tall.")

# Math with variables
birth_year = 2025 - age
print("I was probably born in", birth_year)
