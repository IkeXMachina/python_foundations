# Math with numbers
a = 10
b = 3
sum_result = a + b
difference = a - b
product = a * b
quotient = a / b

print("Sum:", sum_result)
print("Difference:", difference)
print("Product:", product)
print("Quotient:", quotient)

# Playing with strings (text)
name = "Ivan"
greeting = "Hello, " + name + "!"
print(greeting)

# Making it fancy
uppercase_name = name.upper()
lowercase_name = name.lower()
length_of_name = len(name)

print("Uppercase:", uppercase_name)
print("Lowercase:", lowercase_name)
print("Number of letters:", length_of_name)

# Combine text and numbers
age = 25
birth_year = 2024 - age

message = "If you are " + str(age) + ", you were probably born in " + str(birth_year) + "."

print(message)
